x = 10
y = 3

print("მიმატება:", x + y)       # 10 + 3 = 13
print("გამოკლება:", x - y)      # 10 - 3 = 7
print("გამრავლება:", x * y)     # 10 * 3 = 30
print("გაყოფა:", x / y)         # 10 / 3 = 3.3333...
print("მთელრიცხვითი გაყოფა:", x // y)  # 10 // 3 = 3
print("ნაშთი:", x % y)          # 10 % 3 = 1
print("ხარისხი:", x ** y)       # 10 ** 3 = 1000

